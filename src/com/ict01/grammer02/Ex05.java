package com.ict01.grammer02;
class Ex05{
	public static void main(String[] args){
	// 자료형 : DataType : 자바에서 정보를 처리할때 구분하는 형태
	// 1. 기본자료형 : 컴파일러에 의해서 해석되는 자료형
	// boolean, char, byte,	

	// String : 참조자료형이지만 기본자료형처럼 사용
	String str = "Hello java";
	System.out.println(str);

	// String  + 연산 : 더하기 연산이 아니라 문자를 연결하는 역할을 함
	// String에서 + 연산을 하면 결과는 무조건 String

	String k1 = "1000";
	int k2 = 1000;
	System.out.println(k1+10);
	System.out.println(k2+10);

	int s1 = 20;
	int s2 = 4;
	System.out.println(s1 + s2);
	System.out.println("결과 : " + s1 + s2); // 출력 : 결과 : 204
	System.out.println("결과 : " +( s1 + s2)); // 출력 : 결과 :24
	}
}